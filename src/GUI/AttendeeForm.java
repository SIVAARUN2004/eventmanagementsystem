            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dob = sdf.format(jDateChooser1.getDate().getTime());
            String sql= "insert into AttendeeRecord(AttendeeID,FirstName,LastName,DateOfBirth,PhoneNumber,Address,AadharNumber,Gender,Class,CollegeName,Email) values ('"+AttendeeIdField.getText()+"','"+firstNameField.getText()+"','"+lastNameField.getText()+"','"+dob+"','"+PhoneNoField.getText()+"','"+addressField.getText()+"','"+aadharField.getText()+"','"+cmbGender.getSelectedItem()+"','"+ClassField.getText()+"','"+CollegenameField.getText()+"','"+EmailField.getText()+"')";
