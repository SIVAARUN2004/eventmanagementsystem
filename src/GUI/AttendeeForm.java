    private void AttendeeIdFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_AttendeeIdFieldKeyTyped
        char input = evt.getKeyChar();
         if((input<'0' || input>'9') && input!='\b'){
             evt.consume();
             JOptionPane.showMessageDialog(this, "Please enter digits!");
         }
    }//GEN-LAST:event_AttendeeIdFieldKeyTyped

